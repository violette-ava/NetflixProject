public class Profile {
    private String name;
    private int age;
    private String country;

    public Profile(){
        this.name = name;
        this.age - age;
        this.country = country; 
    }

    public Profile(String name, int age, String country){
        this.name = name;
        this.age - age;
        this.country = country; 

    }


    
}
